    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed

        if(saveBtn.getText().equals("Save")&!firstName.getText().equals("First Name")){
            list.add(new CustomerDTO(firstName.getText(),secondName.getText(),address.getText(),tel.getText(),email.getText()));
            String [] data = {firstName.getText(),secondName.getText(),address.getText(),tel.getText(),email.getText()};
            tblM.addRow(data);
        }
        else{
            saveBtn.setText("Save");
            saveBtn.setBackground(Color.CYAN);
            int value = jTable1.getSelectedRow();

            list.get(value).setFirstName(firstName.getText());
            list.get(value).setSecondName(secondName.getText());
            list.get(value).setAddress(address.getText());
            list.get(value).setTel(tel.getText());
            list.get(value).setEmail(email.getText());

            String [] data = {firstName.getText(),secondName.getText(),address.getText(),tel.getText(),email.getText()};
            tblM.removeRow(value);
            tblM.addRow(data);

        }

    }//GEN-LAST:event_saveBtnActionPerformed

