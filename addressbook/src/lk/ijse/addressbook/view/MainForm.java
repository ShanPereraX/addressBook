    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        saveBtn.setText("Update");
        saveBtn.setBackground(Color.YELLOW);
        DefaultTableModel tblM = (DefaultTableModel) jTable1.getModel();

        if(jTable1.getSelectedRowCount()==1){
            int row = jTable1.getSelectedRow();
            firstName.setText(tblM.getValueAt(row, 0).toString());
            secondName.setText(tblM.getValueAt(row, 1).toString());
            address.setText(tblM.getValueAt(row, 2).toString());
            tel.setText(tblM.getValueAt(row, 3).toString());
            email.setText(tblM.getValueAt(row, 4).toString());

        }
    }//GEN-LAST:event_jTable1MouseClicked

    public void filter(String fill){
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel> (tblM);
        jTable1.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(fill));

    }

    private void clearBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clearBtnMouseClicked
        DefaultTableModel tblM = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
            tblM.removeRow(jTable1.getSelectedRow());
            saveBtn.setText("Save");
            saveBtn.setBackground(Color.CYAN);
        }

    }//GEN-LAST:event_clearBtnMouseClicked

